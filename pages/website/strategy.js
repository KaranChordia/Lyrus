document.addEventListener('DOMContentLoaded', () => {
  const sidebarLinks = document.querySelectorAll('.sidebar-nav a');
  const contentArea = document.getElementById('strategy-content-area');
  const mobileMenuBtn = document.getElementById('mobile-menu-btn');
  const mainNav = document.getElementById('main-nav');
  const themeToggle = document.getElementById('theme-toggle');
  
  // Theme functionality (reuse from main)
  if (themeToggle) {
    const sunIcon = document.getElementById('sun-icon');
    const moonIcon = document.getElementById('moon-icon');
    
    // Check saved theme or system preference
    const savedTheme = localStorage.getItem('lyrus-theme');
    const systemPrefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
    
    if (savedTheme === 'dark' || (!savedTheme && systemPrefersDark)) {
      document.documentElement.setAttribute('data-theme', 'dark');
      moonIcon.style.display = 'none';
      sunIcon.style.display = 'block';
    } else {
      document.documentElement.setAttribute('data-theme', 'light');
      moonIcon.style.display = 'block';
      sunIcon.style.display = 'none';
    }
    
    themeToggle.addEventListener('click', () => {
      const currentTheme = document.documentElement.getAttribute('data-theme');
      const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
      
      document.documentElement.setAttribute('data-theme', newTheme);
      localStorage.setItem('lyrus-theme', newTheme);
      
      if (newTheme === 'dark') {
        moonIcon.style.display = 'none';
        sunIcon.style.display = 'block';
      } else {
        moonIcon.style.display = 'block';
        sunIcon.style.display = 'none';
      }
    });
  }

  if (mobileMenuBtn && mainNav) {
    mobileMenuBtn.addEventListener('click', () => {
      const navLinks = document.querySelector('.nav-links');
      if (navLinks) navLinks.classList.toggle('active');
    });
  }
  
  // Navigation active state and content rendering
  function renderContent(category) {
    const data = strategyData[category];
    if (!data) return;
    
    // Fade out
    contentArea.style.opacity = 0;
    
    setTimeout(() => {
      contentArea.innerHTML = `
        <div class="docs-header">
          <div class="docs-icon-large">${data.icon}</div>
          <h1>${data.title}</h1>
        </div>
        <div class="docs-body">
          ${data.content}
        </div>
      `;
      // Fade in
      contentArea.style.opacity = 1;
    }, 200);
  }

  sidebarLinks.forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      
      // Update active state
      sidebarLinks.forEach(l => l.classList.remove('active'));
      link.classList.add('active');
      
      // Render content
      const category = link.getAttribute('data-category');
      renderContent(category);
      
      // Scroll to top
      window.scrollTo({ top: 0, behavior: 'smooth' });
    });
  });

  // Initial render
  renderContent('summary');
});
